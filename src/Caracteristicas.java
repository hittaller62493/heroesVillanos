
public class Caracteristicas {

	private Caracteristica carac;
	private int valor;

	public Caracteristicas(Caracteristica atrib, int i) {
		this.carac = atrib;
		this.valor = i;
	}

	public Caracteristica getCaracteristica() {
		return carac;
	}

	public void setCaracteristica(Caracteristica carac) {
		this.carac = carac;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	

}
